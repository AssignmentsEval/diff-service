package com.limac.diffservice.type;

/**
 * Enumeration for the result types.
 */
public enum ResultType {

    /**
     * EQUAL result type.
     */
    EQUAL,

    /**
     * DIFFERENT_SIZE result type.
     */
    DIFFERENT_SIZE,

    /**
     * SAME_SIZE_BUT_DIFFERENT result type.
     */
    SAME_SIZE_BUT_DIFFERENT
}
